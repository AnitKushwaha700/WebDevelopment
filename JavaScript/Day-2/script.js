
// ------------------ FOR LOOP ---------------- //
for(var i = 1; i <= 10; i++){
    
    console.log(2*i);
    // console.log(i, "=" ,i*i);   
}

// -------------------- WHILE LOOP -----------------//
let b = 1;
while(b<=10){
    console.log(2*b);
    b++;
}

// --------------------- DO WHILE LOOP ------------- //
let c = 1;
do{
    console.log(2*c);
    c++;
}while(c<=10);


